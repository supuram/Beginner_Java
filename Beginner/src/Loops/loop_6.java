package Loops;
public class loop_6 {
    public static void main(String[] args)
    {
        int i,j;
        for(i=1;i<=5;i++)
        {
            for(j=i;j<=5;j++)
            {
                System.out.print(j);
            }
            System.out.println();
        }
        System.out.println("*******************************************");
    }
}